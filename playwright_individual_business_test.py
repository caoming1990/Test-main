INDIVIDUAL_MODULE_NAME = "???????????????????????????"
INDIVIDUAL_HOME_PATH = "/light-accounting/home"
GET_CODE_READY_TEXT = "?????????????????????"
GET_CODE_SENDING_TEXT = "?????????"
GET_CODE_RETRY_TEXT = "????????????"
SKIP_LOGIN_MESSAGE = "????????????????????????????????????????????????"
SCREENSHOT_DIR = Path("playwright_screenshots")
SCREENSHOT_DIR.mkdir(exist_ok=True)


class IndividualBusinessPage(LoginPage):
    def wait_for_individual_home(self) -> None:
        deadline = time.time() + 10
        while time.time() < deadline:
            current_url = self.page.url.lower()
            if INDIVIDUAL_HOME_PATH in current_url:
                return
            self.page.wait_for_timeout(300)
        raise AssertionError("?????????????????????????????????????????????")

    def open_individual_module(self) -> None:
        if self.has_business_module_selector():
            self.select_business_module(INDIVIDUAL_MODULE_NAME)
        self.wait_for_individual_home()

    def module_popup_hidden(self) -> bool:
        try:
            return self.business_module_action_buttons().count() == 0
        except Error:
            return True

    def home_marker_visible(self) -> bool:
        markers = [
            self.page.locator("text=??????").first,
            self.page.locator("text=??????").first,
        ]
        for locator in markers:
            try:
                if locator.is_visible(timeout=1000):
                    return True
            except Error:
                continue
        return False


@pytest.fixture
def individual_business_page(page: Page, request) -> IndividualBusinessPage:
    lp = IndividualBusinessPage(page)
    lp.open()
    lp.page_ready()
    lp.fill_phone(CONFIG.phone)
    lp.click_get_code()

    send_deadline = time.time() + 3
    while time.time() < send_deadline:
        text, is_disabled = lp.get_code_button_state()
        if is_disabled or text != GET_CODE_READY_TEXT or GET_CODE_SENDING_TEXT in text or GET_CODE_RETRY_TEXT in text:
            break
        lp.page.wait_for_timeout(200)

    lp.fill_code(CONFIG.valid_code)
    expect(lp.login_button()).to_be_enabled()
    lp.click_login()

    login_deadline = time.time() + 8
    while time.time() < login_deadline:
        if "/accounting/home" in lp.page.url.lower() and lp.has_business_module_selector():
            break
        lp.page.wait_for_timeout(300)

    if "/accounting/home" not in lp.page.url.lower():
        lp.page.screenshot(path=str(SCREENSHOT_DIR / f"{request.node.name}.png"), full_page=True)
        pytest.skip(SKIP_LOGIN_MESSAGE)

    lp.open_individual_module()
    return lp


